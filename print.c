#include "libft/libft.h"

void	print_w(int i)
{
	while (i > 0)
	{
		ft_putchar(' ');
		i--;
	}
}

void	print_z(int i)
{
	while (i > 0)
	{
		ft_putchar('0');
		i--;
	}
}
